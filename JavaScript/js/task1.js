id='menu11'
var x = document.getElementById('menu1');
var x1 = document.getElementById('menu11')
console.log(x1);
x.onmouseover = function() {
    //x.style.backgroundColor = 'white';
    x1.style.borderColor = 'white';
    x1.style.color = 'black';
    x1.style.fontcolor = 'black';
    x1.style.backgroundColor = 'white';
};
x.onmouseout = function() {
    x.style.backgroundColor = '#383E4C';
    x1.style.borderColor = '#646d7c';
    x1.style.backgroundColor = '#49505F';
    x1.style.color = 'white';
 
};

